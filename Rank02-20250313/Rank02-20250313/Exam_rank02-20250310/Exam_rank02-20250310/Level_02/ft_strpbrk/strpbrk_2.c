char	*ft_strpbrk(const char	*s1, const char	*s2)
{
	int	i;

	while (*s1)
	{
		i = 0;
		while (s2[i] && *s1 != s2[i])
			i++;
		if (s2[i])
			return ((char *)s1);
		s1++;	
	}
	return (0);
}
